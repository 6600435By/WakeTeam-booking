export function sectionsForRole(role: AdminRole): HelpSection[] {
  return HELP_SECTIONS.filter(
    (s) => s.roles === "all" || s.roles.includes(role),
  );
}

export function faqForRole(role: AdminRole): FaqItem[] {
  return FAQ_ITEMS.filter((f) => f.roles === "all" || f.roles.includes(role));
}

export function scenariosForRole(role: AdminRole) {
  return SCENARIOS.filter((s) => s.roles === "all" || s.roles.includes(role));
}
